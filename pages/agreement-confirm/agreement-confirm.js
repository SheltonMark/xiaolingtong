Page({
  data: {
    paragraphs
  },

  handleDecision(decision) {
    wx.setStorageSync('policyHandled', true)
    wx.setStorageSync('policyDecision', decision)
    if (decision === 'agree') {
      wx.setStorageSync('agreedPolicy', true)
    } else {
      wx.removeStorageSync('agreedPolicy')
    }
    wx.redirectTo({ url: '/pages/identity/identity' })
  },

  onAgree() {
    this.handleDecision('agree')
  },

  onDisagree() {
    this.handleDecision('disagree')
  }
})
